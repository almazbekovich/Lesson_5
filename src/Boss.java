public class Boss {

    private int Heal;
    private int damage;
    private String typesOfProtection;


    public int getHeal() {
        return Heal;
    }

    public void setHeal(int heal) {
        Heal = heal;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getTypesOfProtection() {
        return typesOfProtection;
    }

    public void setTypesOfProtection(String typesOfProtection) {
        this.typesOfProtection = typesOfProtection;
    }
}
